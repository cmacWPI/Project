public class Person
{  
   // Declarations
   private String firstName;
   private String lastName;
   
   // Constructor
   public Person(String FN, String LN)
   {  
      firstName = FN;
      lastName = LN;
   }

   public String getFirstName()
   {  
      return firstName;
   }

   public String getLastName()
   {
      return lastName;
   }
}

